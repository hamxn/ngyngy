       <h2>ABOUT ME</h2>
       <p>
          Conrad Roset spent the first part of his 29 years in Terrassa, his native city, among boxes of crayons, felt-tip pens and notebooks; the other part in Barcelona, surrounded by paints, moleskine notebooks, muses, colored pencils, and in the company of his gray cat. Drawing has been his passion and a constant feature in his life, since he played with his brother at drawing everything they liked until, years later, he draw inspiration from women to create the Muses, his most personal collection. “I search the beauty the body exudes, I like drawing the female figure.”<br>
          He received his education at the Joso School and at the Faculty of Fine Arts in Barcelona. Thanks to the spreading of his illustrations through the Internet, he started working for Zara. There, he says, he learnt about his trade, about regularity, and how to study styles of reference illustrators. A year later, he launched himself as a freelance artist, and since then he works for different brands, advertising agencies, and publishing companies.
       </p>
